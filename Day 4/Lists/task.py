# LIst is a data structure in python
list1 = [5,666]
# To  print the whole list just write the list's name
print(list1)
# To write a part of list just do indexing or slicing
print(list1[0])
print(list1[1])
# The slicing with indexing like the one below prints them in the form of a list
print(list1[0:2])
# To change the items in a list just do this
list1[1] = 66685
print(list1)
# To add an item to the end of the list just use append()
list1.append(523)
print(list1)
# To add a list to the end of a list
list1.extend([5])